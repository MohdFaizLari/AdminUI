    emailSpan.innerText = email;
    emailCell.classList.add("tableRowCells");
    emailCell.appendChild(emailSpan);
    tableRow.appendChild(emailCell);

    //-----------------------------Email cell ends here <---------------------------||

    //------------------------------Role cell starts here------------------------------->

